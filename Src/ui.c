// 绘制单个字符
void UI_DrawChar(uint16_t x, uint16_t y, char c, uint16_t color, uint16_t bg_color) 
{
    // 如果超出了直接画个空格
    if (c < ' ' || c > '~') 
    {
        c = ' ';
    }
    
    uint8_t font_index = c - ' ';
    
    // 渲染这个字符的 16 行
    for (uint8_t i = 0; i < 16; i++) 
    {
        uint8_t temp = asc2_1608[font_index][i];
        // 每行 8 个像素点
        for (uint8_t j = 0; j < 8; j++) 
        {
            if (temp & 0x80) { // 最高位为 1，说明有点，画前景色
                ST7789_DrawPixel(x + j, y + i, color);
            } else {           // 否则画背景色
                ST7789_DrawPixel(x + j, y + i, bg_color);
            }
            temp <<= 1; // 移位，判断下一个像素点
        }
    }
}

// 绘制一串字符串
void UI_DrawString(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bg_color) 
{
    uint16_t start_x = x;
    
    while (*str != '\0') 
    {
        if (*str == '\r' || *str == '\n') 
        {
            if (*str == '\n') y += 16; // 高度下移 16 个像素
            x = start_x;               // x 回到起点
            str++;
            continue;
        }
        
        // 绘制普通字符
        UI_DrawChar(x, y, *str, color, bg_color);
        x += 8; // 每个字符宽度 8 像素
        str++;
        
        // 防止文字超出屏幕宽度 (320)
        if (x > 312) {
            x = start_x;
            y += 16;
        }
    }
}