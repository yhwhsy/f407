// 字符映射表
static int GetFontIndex(char c) {
    if(c == ' ') return 0;
    if(c == '%') return 1;
    if(c >= '0' && c <= '9') return 2 + (c - '0');
    if(c == 'L') return 12;
    if(c == 'i') return 13;
    if(c == 'g') return 14;
    if(c == 'h') return 15;
    if(c == 't') return 16;
    if(c == ':') return 17;
    return 0; // 默认返回空格
}

// 在屏幕上画一个 8x16 的字符
static void UI_DrawChar(uint16_t x, uint16_t y, char c, uint16_t color, uint16_t bg_color) {
    int idx = GetFontIndex(c);
    for(int i = 0; i < 16; i++) {
        uint8_t temp = mini_font_8x16[idx][i];
        for(int j = 0; j < 8; j++) {
            if(temp & 0x80) ST7789_DrawPixel(x + j, y + i, color);
            else ST7789_DrawPixel(x + j, y + i, bg_color);
            temp <<= 1;
        }
    }
}

// 显示字符串
void UI_DrawString(uint16_t x, uint16_t y, const char *str, uint16_t color, uint16_t bg_color) {
    while(*str != '\0') {
        UI_DrawChar(x, y, *str, color, bg_color);
        x += 8; // 每个字符宽度 8 像素
        str++;
    }
}